package com.sparta.week01.prac;

public class Prac {

    public static void main(String[] args) {
        // 문자열
        String myName = "김진성";
        System.out.println(myName);

        // 정수
        int age = 24;
        System.out.println(age);

        // 소수점
        float num = 3.7f;
        System.out.println(num);

        // 100억 보다 큰 숫자
        Long bigNum = 10000000000L;
        System.out.println(bigNum);
    }
}
